// button-1
document.getElementById('buy-now-1').addEventListener('click', function(){
    // get product quantity form html tag and convert it number
    const productQuantityField = document.getElementById('product-quantity');
    const productQuantityText = productQuantityField.innerText; 
    const newProductQuantity = parseFloat(productQuantityText);

    // get product balance form html tag and convert it number
    const balanceTotal = document.getElementById('balance-1');
    const balanceText = balanceTotal.innerText;
    const newBalance = parseFloat(balanceText);

    // product quantity 
    const producttotal = newProductQuantity + 1;
    productQuantityField.innerText = producttotal

    // get product price form html tag and convert it number
    const productPriceField = document.getElementById('product-price');
    const productPriceText = productPriceField.innerText;
    const productPriceAmount = parseFloat(productPriceText);

    // product price
    const newProuctPrice = productPriceAmount + newBalance ;
    productPriceField.innerText = newProuctPrice
    
});

